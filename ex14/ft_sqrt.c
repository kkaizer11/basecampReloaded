int	ft_sqrt(int nb)
{
	int	root;

	root = 1;
	if (nb < 0)
		return (0);
	else
	{
		while (root <= 46341)
		{
			if (nb == (root * root))
				return (root);
			root ++;
		}
	}
	return (0);
}
